console.log(10 > 10) // false
console.log(5 <= 5) // true
console.log(10 != 10) //false
console.log(5 != 3) // true

// - == -> hanya mengecek value nya saja
// - === -> mengecek value dan juga tipe datanya
console.log(5 == '5') //true
console.log(3 === '3') //false


